#Realizando comparações e explicando o porquê do resultado das comparações
print("a" > "z") #será falso pois z é o número que está mais proximo do fim do alfabeto
print("z" > "Z") #será true pois números em minúsculo em python são maiores que os maiúsculos
print("mulher" > "homem") #a primeira letra de mulher está mais próximo do fim do alfabeto, então será true
print("Mulher" > "mulher") #considera-se a primeira letra e o maiúsculo e minúsculo, sendo assim será falso